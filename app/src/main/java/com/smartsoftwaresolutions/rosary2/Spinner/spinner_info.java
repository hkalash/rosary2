package com.smartsoftwaresolutions.rosary2.Spinner;

public class spinner_info {

    String Zeker_Id,Zeker_Name_Arabic,Zeker_Name_native;

    public spinner_info(String zeker_Id, String zeker_Name_Arabic) {
        Zeker_Id = zeker_Id;
        Zeker_Name_Arabic = zeker_Name_Arabic;
    }

    public String getZeker_Id() {
        return Zeker_Id;
    }

    public void setZeker_Id(String zeker_Id) {
        Zeker_Id = zeker_Id;
    }

    public String getZeker_Name_Arabic() {
        return Zeker_Name_Arabic;
    }

    public void setZeker_Name_Arabic(String zeker_Name_Arabic) {
        Zeker_Name_Arabic = zeker_Name_Arabic;
    }

    public String getZeker_Name_native() {
        return Zeker_Name_native;
    }

    public void setZeker_Name_native(String zeker_Name_native) {
        Zeker_Name_native = zeker_Name_native;
    }
}
